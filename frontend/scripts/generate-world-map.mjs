/**
 * Generates app/utils/world-map-data.ts — a dotted world map with real
 * landmass geography plus projected coordinates for the cities CODECV
 * serves. Run whenever the city list or map density changes:
 *
 *   node scripts/generate-world-map.mjs
 */
import { writeFileSync } from 'node:fs'
import { fileURLToPath } from 'node:url'
import { dirname, join } from 'node:path'
import DottedMap from 'dotted-map'

// Coordinate scale applied to the dotted-map grid so the SVG viewBox uses
// comfortable numbers for stroke widths and label font sizes.
const SCALE = 10
const MAP_HEIGHT = 48 // grid rows — density/definition of the map

// Label offsets keep the dense European cluster readable.
const CITIES = [
  { name: 'Dublin', lat: 53.35, lng: -6.26, labelDx: -12, labelDy: -14 },
  { name: 'London', lat: 51.507, lng: -0.128, labelDx: 6, labelDy: 26 },
  { name: 'Berlin', lat: 52.52, lng: 13.405, labelDx: 16, labelDy: -8 },
  { name: 'New York', lat: 40.713, lng: -74.006, labelDx: -12, labelDy: 4 },
  { name: 'São Paulo', lat: -23.55, lng: -46.633, labelDx: 12, labelDy: 4 },
  { name: 'Tokyo', lat: 35.676, lng: 139.65, labelDx: 12, labelDy: 4 },
  { name: 'Sydney', lat: -33.868, lng: 151.209, labelDx: 12, labelDy: 4 },
]

const map = new DottedMap({ height: MAP_HEIGHT, grid: 'diagonal' })

// Land dots first — addPin() would append the pins to the same list.
const dots = map
  .getPoints()
  .map(p => [Math.round(p.x * SCALE * 10) / 10, Math.round(p.y * SCALE * 10) / 10])

const cities = CITIES.map(({ name, lat, lng, labelDx, labelDy }) => {
  const pin = map.addPin({ lat, lng })
  return {
    name,
    x: Math.round(pin.x * SCALE * 10) / 10,
    y: Math.round(pin.y * SCALE * 10) / 10,
    labelDx,
    labelDy,
  }
})

const maxX = Math.max(...dots.map(d => d[0]))
const maxY = Math.max(...dots.map(d => d[1]))
const viewBox = `0 0 ${Math.ceil(maxX + SCALE)} ${Math.ceil(maxY + SCALE)}`

const file = `/* AUTO-GENERATED by scripts/generate-world-map.mjs — do not edit by hand.
 * Dotted world map (real landmass geography via the dotted-map package)
 * plus projected pin coordinates for the cities CODECV serves.
 */

export interface WorldCity {
  name: string
  x: number
  y: number
  /** label offset relative to the pin, keeps dense clusters readable */
  labelDx: number
  labelDy: number
}

export const WORLD_VIEWBOX = '${viewBox}'
export const WORLD_DOT_RADIUS = ${Math.round(SCALE * 0.28 * 10) / 10}

export const WORLD_CITIES: ReadonlyArray<WorldCity> = ${JSON.stringify(cities, null, 2)}

export const WORLD_DOTS: ReadonlyArray<readonly [number, number]> = [
${dots.map(d => `  [${d[0]}, ${d[1]}],`).join('\n')}
]
`

const out = join(dirname(fileURLToPath(import.meta.url)), '../app/utils/world-map-data.ts')
writeFileSync(out, file)
console.log(`wrote ${out}: ${dots.length} dots, ${cities.length} cities, viewBox ${viewBox}`)
